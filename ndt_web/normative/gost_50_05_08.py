def format_control_sequence() -> str:
    """Последовательность контроля (п. 7.2.8)."""
    steps = '; '.join(f'{k}: {v}' if isinstance(v, str) else f'{k}: {", ".join(v)}'
                      for k, v in CONTROL_PROCEDURE_STEPS.items())
    return f'{DOCUMENT_CODE}, п. 7.2.8: {steps}.'


def format_term(clause: str) -> Optional[str]:
    """Определение термина с ссылкой на DOCUMENT_CODE."""
    entry = get_term(clause)
    if not entry:
        return None
    return f'{DOCUMENT_CODE}, п. {clause} — {entry["term"]}: {entry["definition"]}'


def format_tech_card_items() -> str:
    items = '; '.join(TECH_CARD_REQUIRED_ITEMS)
    return f'{DOCUMENT_CODE}, приложение В — технологическая карта ВИК должна содержать: {items}.'


