def get_hermeticity_class(code: str) -> Optional[dict]:
    """Класс герметичности по коду I–V (табл. 1)."""
    if not code:
        return None
    key = code.strip().upper()
    return HERMETICITY_CLASSES.get(key)


def get_table3_methods(class_code: str) -> Optional[tuple]:
    """Способы и требования табл. 3 для класса герметичности."""
    info = get_hermeticity_class(class_code)
    if not info:
        return None
    block = TABLE_3_CLASS_METHODS.get(info['code'])
    if not block:
        return None
    return block['sensitivity_range'], block['methods']


def get_penetrant_contact_min(class_code: str, thickness_mm: float) -> Optional[int]:
    """Минимальное время контакта с проникающей жидкостью, мин (табл. 4)."""
    table = TABLE_4_PENETRANT_CONTACT_MIN.get((class_code or '').strip().upper())
    if not table:
        return None
    limits = sorted(table.keys())
    for lim in limits:
        if thickness_mm <= lim:
            return table[lim]
    return table[limits[-1]]


def get_kerosene_contact_min(position: str, thickness_mm: float) -> Optional[int]:
    """Время контакта с керосином, мин (табл. 5)."""
    pos = (position or 'lower').strip().lower()
    row = TABLE_5_KEROSENE_CONTACT_MIN.get(pos) or TABLE_5_KEROSENE_CONTACT_MIN['lower']
    if thickness_mm <= 6:
        return row[6]
    if thickness_mm <= 24:
        return row[24]
    return row['over_24']


def get_helium_hold_min(volume_m3: float) -> int:
    """Выдержка под давлением по объёму, мин (п. 7.2.3.6)."""
    for v_min, v_max, t_min in HELIUM_HOLD_TIME_BY_VOLUME_MIN:
        if volume_m3 > v_min and (v_max is None or volume_m3 <= v_max):
            return t_min
    return HELIUM_HOLD_TIME_BY_VOLUME_MIN[-1][2]


