def all_kb_chunks() -> list[tuple[str, str]]:
    """Эталонные фрагменты для RAG/ingest."""
    chunks: list[tuple[str, str]] = [
        ('п. 1.1–1.4 область применения', format_scope()),
        ('п. 4.8 уровни компетентности', format_competence_levels()),
        ('раздел 5 требования к кандидату', format_candidate_requirements()),
        ('раздел 6 порядок оценки компетентности', format_assessment_procedure()),
        ('раздел 7 аттестационные экзамены', format_exam_requirements()),
        ('п. 8.1–8.5 сроки удостоверений', format_validity_periods()),
        ('раздел 4 организации аттестации', format_organizations()),
    ]
    for para, row in TERMS.items():
        short = row.get('short') or row.get('term', '')[:40]
        chunks.append((
            f'п. {para} термин — {short}',
            f'{DOCUMENT_CODE}, п. {para} {row["term"]}: {row["definition"]}'
            + (f' Примечание: {row["note"]}' if row.get('note') else ''),
        ))
    for method in TABLE_A2_TRAINING_HOURS:
        chunks.append((f'табл. А.2 {method}', format_table_a2(method)))
    for method in TABLE_A3_EXPERIENCE_MONTHS:
        chunks.append((f'табл. А.3 {method}', format_table_a3(method)))
    for method, row in TABLE_G1_GENERAL_EXAM.items():
        chunks.append((
            f'табл. Г.1 общий экзамен — {method}',
            (
                f'{DOCUMENT_CODE}, табл. Г.1, общий экзамен, «{method}»: '
                f'в сборнике > {row["bank_min"]} вопросов; на экзамене БПВЗ — {row["БПВЗ"]}, '
                f'СПВЗ — {row["СПВЗ"]}, СПА — {row["СПА"]}.'
            ),
        ))
    return chunks
