def get_measurement_tolerance_mm(value_mm: float) -> Optional[float]:
    """Погрешность по табл. 1 для измеряемой величины, мм."""
    if value_mm is None or value_mm < 0:
        return None
    for row in TABLE_1_MEASUREMENT_TOLERANCE_MM:
        limit = row['range_max_mm']
        if limit is None or value_mm <= limit:
            return row['tolerance_mm']
    return TABLE_1_MEASUREMENT_TOLERANCE_MM[-1]['tolerance_mm']


def get_term(clause: str) -> Optional[dict]:
    """Термин по номеру пункта §3."""
    return TERMS.get((clause or '').strip())


def format_table_1() -> str:
    """Текст табл. 1 для RAG/tools."""
    lines = [f'{DOCUMENT_CODE}, таблица 1 — предельно допустимая погрешность измерений при ИК:']
    prev = 0.0
    for row in TABLE_1_MEASUREMENT_TOLERANCE_MM:
        if row['range_max_mm'] is None:
            lines.append(f'  св. {prev:g} мм — ±{row["tolerance_mm"]:g} мм.')
        else:
            lines.append(
                f'  св. {prev:g} до {row["range_max_mm"]:g} мм включ. — ±{row["tolerance_mm"]:g} мм.'
            )
            prev = row['range_max_mm']
    lines.append(
        'Если поле допуска выходит за таблицу — погрешность по строке наибольшего предельного размера (примечание).'
    )
    return ' '.join(lines)


