"""
seed_golden_answers.py — слой эталонных якорей (golden answers).

Обратный инжиниринг: точные ответы на типовые вопросы экзамена/практики
с указанием конкретных пунктов НД. Помечаются is_golden=True и получают
приоритет в retrieval/orchestrator, чтобы консультант цитировал ИМЕННО
эти пункты (без замены на похожие).

Запуск:
  python3 seed_golden_answers.py
Идемпотентно (по section_label).
"""
import os
import sys

sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

import django
os.environ.setdefault('DJANGO_SETTINGS_MODULE', 'ndt_project.settings')
os.environ.setdefault('ALLOWED_HOSTS', 'testserver,127.0.0.1,localhost')
django.setup()

from ai_consultant.models import DocumentSource, DocumentChunk
from ai_consultant.services.embeddings import embed_texts, get_embedding_model_name


GOLDEN_DOC_NUMBER = 'GOLDEN'
GOLDEN_TITLE = 'Эталонные ответы (golden anchors, обратный инжиниринг)'

# (section_label, текст-якорь с точной ссылкой) — БЕЗ слова GOLDEN в метке,
# чтобы LLM воспринимал якорь как обычный пункт НД; приоритет даёт is_golden.
GOLDEN = [
    (
        "ГОСТ Р 50.05.07-2018, п. 6.1.16 + ГОСТ 7512-82, п. 3.9 — канавочный эталон на трубе ≤100 мм",
        "Вопрос: как разрешается устанавливать канавочный эталон на трубопровод "
        "диаметром до 100 мм? Ответ: канавочный (режущий) эталон устанавливают "
        "вдоль оси трубы без установки ограничительных меток на границах "
        "контролируемых участков. Нормы: ГОСТ Р 50.05.07-2018, п. 6.1.16 "
        "(установка канавочного эталона на трубопроводе Dн ≤ 100 мм вдоль оси "
        "трубы) и ГОСТ 7512-82, п. 3.9 (канавочный/режущий эталон чувствительности).",
    ),
    (
        "ГОСТ Р 50.05.07-2018, п. 6.3.12 + ГОСТ 7512-82, п. 5.6 — перекрытие снимков",
        "Вопрос: какая должна быть длина перекрытия изображений смежных участков "
        "сварных соединений при длине контролируемого участка свыше 100 мм? "
        "Ответ: не менее 20 мм. Нормы: ГОСТ Р 50.05.07-2018, п. 6.3.12 "
        "(перекрытие изображений смежных участков — не менее 20 мм) и "
        "ГОСТ 7512-82, п. 5.6 (требования к радиографическим снимкам).",
    ),
    (
        "ГОСТ Р 50.05.07-2018, п. 6.3.13 — околошовная зона (ОШЗ) 5 мм",
        "Вопрос: какая должна быть околошовная зона для сварных соединений "
        "толщиной 13 мм? Ответ: ширина околошовной зоны (ОШЗ) при "
        "радиографическом контроле — не менее 5 мм в каждую сторону от оси шва "
        "(итого полоса контроля 5 мм + шов + 5 мм). Норма: ГОСТ Р 50.05.07-2018, "
        "п. 6.3.13 (ширина контролируемой зоны снимка включает околошовную зону "
        "не менее 5 мм по обе стороны от шва).",
    ),
    (
        "НП-105-18, п. 7.4 — окно рентгеновской трубки из бериллия",
        "Вопрос: из какого материала изготовлено выходное окно высоковольтных "
        "рентгеновских трубок? Ответ: из бериллия (Be) — малое поглощение "
        "тормозного излучения в мягкой части спектра. Норма: НП-105-18, п. 7.4 "
        "(выходное окно высоковольтных рентгеновских трубок изготавливается "
        "из бериллия).",
    ),
    (
        "ГОСТ 7512-82, п. 2.13 — первая цифра маркировки ИКИ = материал",
        "Вопрос: что обозначает первая цифра маркировочного знака ИКИ "
        "(эталона чувствительности)? Ответ: первая цифра обозначает КОД МАТЕРИАЛА "
        "эталона (1 — сплавы на основе железа, 2 — алюминий и магний, 3 — титан, "
        "4 — медь, 5 — никель); следующие 1–2 цифры — номер эталона (1–4). "
        "Норма: ГОСТ 7512-82, п. 2.13 (маркировка проволочного ИКИ: первая цифра "
        "— материал, далее — номер эталона).",
    ),
]


def main():
    src, _ = DocumentSource.objects.get_or_create(
        doc_number=GOLDEN_DOC_NUMBER,
        defaults={'title': GOLDEN_TITLE, 'doc_type': 'gost'},
    )
    model = get_embedding_model_name()
    existing = set(
        DocumentChunk.objects.filter(source=src).values_list('section_label', flat=True)
    )
    added = 0
    for lbl, txt in GOLDEN:
        if lbl in existing:
            continue
        vecs = embed_texts([txt])
        DocumentChunk.objects.create(
            source=src,
            chunk_index=DocumentChunk.objects.filter(source=src).count(),
            section_label=lbl,
            text=txt,
            embedding=vecs[0] if vecs else None,
            embedding_model_version=model,
            is_golden=True,
        )
        added += 1
    print(f"Golden-якорей добавлено: {added} (всего в source: "
          f"{DocumentChunk.objects.filter(source=src).count()})")


if __name__ == '__main__':
    main()
