def format_term(clause: str) -> Optional[str]:
    """Определение термина по номеру пункта §3."""
    entry = TERMS.get(clause)
    if not entry:
        return None
    text = f'{DOCUMENT_CODE}, п. {clause} — {entry["term"]}: {entry["definition"]}'
    note = entry.get('note') or entry.get('notes')
    if note:
        text += f' Примечание: {note}.'
    return text


