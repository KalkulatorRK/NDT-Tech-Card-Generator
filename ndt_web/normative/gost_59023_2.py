def get_joint_type_choices():
    """Список кортежей для поля выбора типа сварного соединения."""
    return [(key, f"{info['code']} — {info['name']}") for key, info in JOINT_TYPES.items()]


def get_welding_process_choices():
    """Список кортежей для поля выбора вида сварки."""
    return [(p['code'], f"{p['code']} — {p['name']}") for p in WELDING_PROCESSES]


def get_material_choices():
    """Список марок сталей для выпадающего списка."""
    return [(g, g) for g in ALL_STEEL_GRADES]


def get_pipe_diameters():
    """Список наружных диаметров труб для выбора."""
    return [(str(size[1]), f"Ду{size[0]} (Dн={size[1]} мм)") for size in PIPE_SIZES]
