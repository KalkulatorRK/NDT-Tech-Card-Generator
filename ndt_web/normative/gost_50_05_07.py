SENSITIVITY_TABLE = [
    (5,         1.5,    2.0,    3.0),
    (10,        1.0,    1.5,    2.0),
    (25,        0.8,    1.0,    1.5),
    (50,        0.6,    0.8,    1.2),
    (100,       0.5,    0.7,    1.0),
    (float('inf'), 0.4, 0.6,    0.8),
]


def get_sensitivity(thickness_mm: float, control_class: str) -> float:
    """
    Возвращает минимальное требуемое значение чувствительности (% от толщины)
    для заданной толщины и класса контроля.

    :param thickness_mm: толщина контролируемого материала, мм
    :param control_class: класс контроля ('A', 'B' или 'C')
    :return: значение чувствительности в процентах
    """
    class_index = {'A': 1, 'B': 2, 'C': 3}
    idx = class_index.get(control_class, 3)
    for row in SENSITIVITY_TABLE:
        if thickness_mm <= row[0]:
            return row[idx]
    return SENSITIVITY_TABLE[-1][idx]


def get_sensitivity_mm(thickness_mm: float, control_class: str) -> float:
    """
    Возвращает минимально выявляемый дефект в мм для заданной толщины и класса.

    :param thickness_mm: толщина, мм
    :param control_class: класс ('A', 'B', 'C')
    :return: размер выявляемого дефекта, мм
    """
    sensitivity_pct = get_sensitivity(thickness_mm, control_class)
    return round(thickness_mm * sensitivity_pct / 100, 3)


