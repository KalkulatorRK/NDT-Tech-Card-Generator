"""
Машиночитаемое описание методики генератора техкарт РГК («Карта-НК»).

Вставляется в системный промпт ИИ-консультанта, чтобы ответы по расчётам,
схемам, S/S1, K, f/N/L и техкартам совпадали с логикой приложения
(techcards.generator + normative.*), а не с произвольной интерпретацией
справочников.
"""

from __future__ import annotations

# Компактный блок для system prompt (всегда в контексте LLM)
GENERATOR_METHODOLOGY_BLOCK = """
=== МЕТОДИКА ГЕНЕРАТОРА ТЕХКАРТ РГК ПРИЛОЖЕНИЯ «КАРТА-НК» ===
Отвечая на вопросы о расчётах, схемах просвечивания, техкартах, подборе
параметров, чувствительности, расточке кромок — опирайся на эту методику
как на описание того, как работает генератор. Числа и нормы подтверждай
ссылками на НД (ГОСТ / НП), не на справочники.

1) ТИПЫ СОЕДИНЕНИЙ — ГОСТ Р 59023.2-2020
   • Каталог типов (С-*, У-*, Т-*, ТС-*) и размеры шва (e, g) — из таблиц ГОСТ.
   • Большинство таблиц: колонка «S = S1» (номинальная толщина = толщина в зоне шва).
   • Внутренняя расточка кромок (калибровка конца трубы): S ≠ S1.
     В приложении расточка по чертежу табл. 9.30 задана для С-23-2
     (типоразмер Dн×S → Dр, минимальная толщина S1).
     С-22-2 в той же табл. 9.30 — БЕЗ расточки, S = S1.
   • Не путать с «выемкой» под спецсхему доступа: расточка кромок — это
     уменьшение толщины стенки в зоне шва до S1, а не выемка ≥5 мм на рис. схем.
   • Эффективная толщина для РГК: S_eff = S1 при расточке, иначе S_eff = S.

2) РАДИАЦИОННАЯ ТОЛЩИНА И K — НП-105-18, ГОСТ Р 50.05.07-2018
   • Для K (табл. чувствительности): при одной стенке S_K = S_eff + g_min + Sпк;
     при двух стенках S_K = S_eff + S_eff.
   • Для расчёта f: S_рад(f) = S_eff + g_max (1 стенка) или 2·S_eff + 2·g_max (2 стенки).
   • Источник/плёнка (табл. Б.1–Б.3 ГОСТ Р 50.05.07) — по S_рад(f).
   • Нормы пор/шлаков (п. 10.2 техкарты) — по толщине оценки (S или явно
     заданная Sмин/S1); выпуклость/вогнутость корня — по номинальной S и Ду
     (табл. 4.3 / 4.5 НП-105, не 4.6).

3) СХЕМЫ ПРОСВЕЧИВАНИЯ — ГОСТ Р 50.05.07-2018, приложение Г
   • Коды генератора: 5a→3а, 5b→3б, 5v→3в, 5g→3г, 5d→3д, 5zh→3ж, 4_6→черт. 2.
   • f, N, L считаются формулами приложения Г (в т.ч. 5г: f ≥ 0,5·[1,5·C·(D−d)−D],
     C = max(2Φ/K, 4); N — по f/D из табл. Г.4).
   • При переносе таблицы на новую страницу DOCX зазор от колонтитула —
     через поля страницы; п. 6.9 схемы — с новой страницы.

4) ИКИ — ГОСТ 7512-82 + ГОСТ Р 50.05.07 п. 6.1.11
   • Выбор проволоки по радиационной толщине и требуемому K.
   • ИКИ со стороны плёнки: ужесточение на 1 ступень проволоки; геометрия
     (f, C) пересчитывается по ужесточённому K.

5) ПОТОК ГЕНЕРАТОРА
   Ввод (материал, S, Dн, тип соединения ГОСТ 59023.2, способ сварки,
   категория, схема, источник, Φ, OFD, сторона ИКИ) → зоны e/g по ГОСТ →
   S_eff (расточка) → S_K и K → источники табл. Б → f/N/L → Ug →
   нормы НП-105 → заполнение DOCX по шаблону.
   Полная генерация DOCX/PDF — только в веб-мастере техкарт; в чате —
   объяснение методики и точечные расчёты, без подмены формул справочником.

6) ПРИОРИТЕТ ПРИ КОНФЛИКТЕ
   НД (ГОСТ, НП) и методика генератора выше справочной литературы.
   Справочники можно использовать только как фон для пояснений, БЕЗ ссылок
   в ответе.
=== КОНЕЦ МЕТОДИКИ ГЕНЕРАТОРА ===
""".strip()


def get_generator_methodology_block() -> str:
    """Блок для вставки в system prompt."""
    return GENERATOR_METHODOLOGY_BLOCK


def question_needs_generator_context(question: str) -> bool:
    """Эвристика: вопрос про расчёты / техкарту / параметры РГК."""
    import re
    q = (question or '').lower()
    return bool(re.search(
        r"(техкарт|генератор|расчёт|расчет|схем[аыуе]\s*(просвеч|3[абвгдж]|контроль)|"
        r"чувствительн|фокусн|экспозиц|источник\s+излучен|плёнк|пленк|"
        r"расточ|s\s*=\s*s1|s1\b|толщин\w*\s+стенк|подбер\w*\s+режим|"
        r"параметр\w*\s*(съёмк|съемк|ргк|контрол)|ики|нерезк|ofd|sfd|"
        r"категор\w*\s*[ivx]{1,3}|табл\.?\s*б\.|50\.05\.07|7512|59023)",
        q,
        re.IGNORECASE,
    ))
